<?php

// Define um array com os números de 1 a 5
$arr = [1, 2, 3, 4, 5];

// Reverte a ordem dos elementos do array
$arrRev = array_reverse($arr);

// Imprime o array original
print_r($arr);

// Imprime o array invertido
print_r($arrRev);

// Define um array com diferentes tipos de dados
$arr2 = ['Rafael', 33, true, [3, 2]];

// Reverte a ordem dos elementos do segundo array
$arr2Rev = array_reverse($arr2);

// Imprime o segundo array invertido
print_r($arr2Rev);


