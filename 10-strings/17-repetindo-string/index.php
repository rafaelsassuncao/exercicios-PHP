<?php

// 1º Modo:
// Definindo a string que será repetida
$str = "Teste";

// Utilizando a função str_repeat para repetir a string $str 5 vezes
$strRepetida = str_repeat($str, 5);

// Exibindo a string repetida seguida de uma quebra de linha (<br>)
echo $strRepetida . "<br>";

// 2º Modo
// Definindo a frase que será repetida
$frase = "Testanto repetição por frase";

// Utilizando a função str_repeat para repetir a string $frase 5 vezes
echo str_repeat($frase, 5);
?>
